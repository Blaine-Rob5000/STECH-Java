//display the area and circumference (perimeter) of a circle with radius 5.5

public class Exercise_1_8 {
	public static void main(String[] args) {
		System.out.println("Circle Radius:  5.5");
		System.out.println("");
		System.out.println("Area:");
		System.out.println(Math.PI * 5.5 * 5.5);
		System.out.println("");
		System.out.println("Circumference:");
		System.out.println(2 * Math.PI * 5.5);
		System.out.println("");
	}
}
